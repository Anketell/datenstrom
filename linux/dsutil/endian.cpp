uint16_t htob( uint16_t u )
{
   return htons( u );
}

//-----------------------------------------------------------------------------

uint32_t htob( uint32_t u )
{
   return htonl( u );
}

//-----------------------------------------------------------------------------

uint64_t htob( uint64_t u )
{
   return ( ( uint64_t )htonl( u & 0xffffffff ) << 32 ) | htonl( u >> 32 );
}

//-----------------------------------------------------------------------------

int16_t htob( int16_t i )
{
   return static_cast< int16_t >( htons( static_cast< uint16_t >( i ) ) );
}

//-----------------------------------------------------------------------------

int32_t htob( int32_t i )
{
   return static_cast< int32_t >( htonl( static_cast< uint32_t >( i ) ) );
}

//-----------------------------------------------------------------------------

int64_t htob( int64_t i )
{
   return static_cast< int64_t >( htob( static_cast< uint64_t >( i ) ) );
}


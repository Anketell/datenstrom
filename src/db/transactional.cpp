#include <db/transactional.h>

//-----------------------------------------------------------------------------

namespace ds::db
{

//-----------------------------------------------------------------------------

void transactional::begin_transaction( void )
{
   m_txn_count++;
}

//-----------------------------------------------------------------------------

void transactional::commit_transaction( void )
{
   m_txn_count--;
}

//-----------------------------------------------------------------------------

void transactional::rollback_transaction( void )
{
   m_txn_count--;
}

//-----------------------------------------------------------------------------

bool transactional::in_transaction( void ) const
{
   return m_txn_count > 0;
}

//-----------------------------------------------------------------------------

}

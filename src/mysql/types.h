#ifndef DS_MYSQL_TYPES_H
#define DS_MYSQL_TYPES_H

//-----------------------------------------------------------------------------

#include <mysql/mysql.h>

//-----------------------------------------------------------------------------

namespace ds
{

//-----------------------------------------------------------------------------

namespace mysql
{

//-----------------------------------------------------------------------------

struct bind_info_t
{
   unsigned long length;
   my_bool       is_null;
   my_bool       error;
};

//-----------------------------------------------------------------------------

struct stmt_t
{
   MYSQL_STMT  * stmt       = nullptr;
   MYSQL_BIND  * mysql_bind = nullptr;
   bind_info_t * bind_info  = nullptr;
   int           count      = 0;

   ~stmt_t( void )
   {
      if ( stmt )
         mysql_stmt_close( stmt );

      stmt = nullptr;
   }
};

//-----------------------------------------------------------------------------

}

//-----------------------------------------------------------------------------

}

//-----------------------------------------------------------------------------

#endif

//-----------------------------------------------------------------------------

struct stmt_t
{
   MYSQL_STMT * stmt = nullptr;

   ~stmt_t( void )
   {
      if ( stmt )
         mysql_stmt_close( stmt );

      stmt = nullptr;
   }
};

//-----------------------------------------------------------------------------

struct bind_info_t
{
   unsigned long length;
   my_bool       is_null;
   my_bool       error;
};


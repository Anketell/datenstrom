//-----------------------------------------------------------------------------

#include <sqlite/error.h>
#include <sqlite3.h>
#include <stdexcept>

//-----------------------------------------------------------------------------

namespace ds
{

//-----------------------------------------------------------------------------

namespace sqlite
{

//-----------------------------------------------------------------------------

[[noreturn]] void throw_error( const std::string & operation, const char * error )
{
   throw std::runtime_error(  operation + " failed: " + error );
}

//-----------------------------------------------------------------------------

[[noreturn]] void throw_error( const std::string & operation, int rc )
{
   throw_error( operation, sqlite3_errstr( rc ) );
}

//-----------------------------------------------------------------------------

}

//-----------------------------------------------------------------------------

}

#ifndef DS_SQLITE_TYPES_H
#define DS_SQLITE_TYPES_H

//-----------------------------------------------------------------------------

#include <sqlite3.h>

//-----------------------------------------------------------------------------

namespace ds
{

//-----------------------------------------------------------------------------

namespace sqlite
{

//-----------------------------------------------------------------------------

struct stmt_t
{
   sqlite3_stmt * stmt = nullptr;

   ~stmt_t( void )
   {
      if ( stmt )
         sqlite3_finalize( stmt );

      stmt = nullptr;
   }
};

//-----------------------------------------------------------------------------

}

//-----------------------------------------------------------------------------

}

//-----------------------------------------------------------------------------

#endif

#pragma once

//-----------------------------------------------------------------------------

#include <tuple>
#include <ds/ds.h>

//-----------------------------------------------------------------------------

namespace std
{

//-----------------------------------------------------------------------------

template< typename... T >
ds::istream & operator >> ( ds::istream & in, tuple< T... > & t )
{
   std::apply( [ & ]( auto &... args )
   {
      ( ( in >> args ), ... );
   }, t );

   return in;
}

//-----------------------------------------------------------------------------

template< typename... T >
ds::ostream & operator << ( ds::ostream & out, const tuple< T... > & t )
{
   std::apply( [ & ]( const auto &... args )
   {
      ( ( out << args ), ... );
   }, t );

   return out;
}

//-----------------------------------------------------------------------------

}

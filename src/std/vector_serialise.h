#pragma once

//-----------------------------------------------------------------------------

#include <vector>
#include <ds/ds.h>

//-----------------------------------------------------------------------------

namespace std
{

//-----------------------------------------------------------------------------

template< typename T >
ds::ostream & operator << ( ds::ostream & out, const vector< T > & v )
{
   for ( auto & t : v )
      out << t << ds::endr;

   return out;
}

//-----------------------------------------------------------------------------

template< typename T >
ds::istream & operator >> ( ds::istream & in, vector< T > & v )
{
   while ( !in.eof() )
   {
      v.push_back( in );
      in >> ds::endr;
   }

   return in;
}

//-----------------------------------------------------------------------------

}
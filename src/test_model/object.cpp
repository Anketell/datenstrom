bool Object::operator==( const Object & o ) const
{
   return m_i8    == o.m_i8    &&
          m_i16   == o.m_i16   &&
          m_i32   == o.m_i32   &&
          m_i64   == o.m_i64   &&
          m_u8    == o.m_u8    &&
          m_u16   == o.m_u16   &&
          m_u32   == o.m_u32   &&
          m_u64   == o.m_u64   &&
          fabs( m_f - o.m_f ) < 0.001 &&
          m_d     == o.m_d     &&
          m_hello == o.m_hello;
}


#include <dsutil/endian.h>
#include <winsock2.h>

//-----------------------------------------------------------------------------

namespace ds::endian
{

//-----------------------------------------------------------------------------

uint16_t htol( uint16_t u )
{
   return u;
}

//-----------------------------------------------------------------------------

uint32_t htol( uint32_t u )
{
   return u;
}

//-----------------------------------------------------------------------------

uint64_t htol( uint64_t u )
{
   return u;
}

//-----------------------------------------------------------------------------


uint16_t htob( uint16_t u )
{
   return htons( u );
}

//-----------------------------------------------------------------------------

uint32_t htob( uint32_t u )
{
   return htonl( u );
}

//-----------------------------------------------------------------------------

uint64_t htob( uint64_t u )
{
   return htonll( u );
}

//-----------------------------------------------------------------------------

}

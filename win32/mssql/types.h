//-----------------------------------------------------------------------------

#ifndef DS_MSSQL_TYPES_H
#define DS_MSSQL_TYPES_H

//-----------------------------------------------------------------------------

#include <sql.h>

//-----------------------------------------------------------------------------

namespace ds
{

//-----------------------------------------------------------------------------

namespace mssql
{

//-----------------------------------------------------------------------------

struct stmt_t
{
   SQLHSTMT hstmt = nullptr;

   ~stmt_t( void )
   {
      if ( hstmt )
         SQLFreeHandle( SQL_HANDLE_STMT, hstmt );

      hstmt = nullptr;
   }
};

//-----------------------------------------------------------------------------

}

//-----------------------------------------------------------------------------

}

//-----------------------------------------------------------------------------

#endif
